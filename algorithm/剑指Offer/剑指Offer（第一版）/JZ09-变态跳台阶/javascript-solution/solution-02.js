/**
 * 解法二：数学定义
 * - 青蛙只跳1或2可以得出是一个斐波那契问题，即a[n]=a[n-1]+a[n-2],那么能跳1,2,3个台阶时a[n]=a[n-1]+a[n-2]+a[n-3]。
 *   那么有：
 *   a[n]=a[n-1]+a[n-2]+......+a[1] ..........................①
 *   a[n-1]=     a[n-2]+......+a[1] ..........................②
 *   两式相减可得：a[n]=2*a[n-1]
 */

function jumpFloorII(number)
{
    // write code here
    return Math.pow(2, number - 1);
}